public class ListItem {

    String displayname;
    String id;
    String accountno;
    String externalid;
    String officeid;
    String officename;

    public ListItem(String displayname, String id,
            String accountno,
            String externalid,
            String officeid,
            String officename
    ) {
        {

            this.displayname=displayname;
            this.id=id;
            this.accountno=accountno;
            this.externalid=externalid;
            this.officeid=officeid;
            this.officename=officename;

        }
    }


}
